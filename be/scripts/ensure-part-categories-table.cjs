const sql = `
IF NOT EXISTS (SELECT 1 FROM sys.tables WHERE name = 'part_categories')
BEGIN
  CREATE TABLE part_categories (
    part_category_id INT IDENTITY(1,1) PRIMARY KEY,
    category_code VARCHAR(50) NOT NULL UNIQUE,
    category_name NVARCHAR(255) NOT NULL,
    description NVARCHAR(1000) NULL,
    status VARCHAR(50) NOT NULL DEFAULT 'active',
    minimum_threshold INT NOT NULL DEFAULT 20,
    average_price DECIMAL(18,2) NOT NULL DEFAULT 0,
    total_value DECIMAL(18,2) NOT NULL DEFAULT 0,
    created_at DATETIME NOT NULL DEFAULT GETDATE(),
    updated_at DATETIME NOT NULL DEFAULT GETDATE()
  );
END

INSERT INTO part_categories (category_code, category_name, description)
SELECT DISTINCT p.part_category, p.part_category, 'Autogenerated from parts table'
FROM parts p
WHERE p.part_category IS NOT NULL
  AND p.part_category <> ''
  AND NOT EXISTS (
    SELECT 1 FROM part_categories pc WHERE pc.category_code = p.part_category
  );
`;

connection.on('connect', (err) => {
  if (err) {
    console.error('connect error', err);
    process.exit(1);
  }

  const request = new Request(sql, (error) => {
    if (error) {
      console.error('request error', error);
      process.exit(1);
    }
    console.log('part_categories table is ready');
    connection.close();
  });

  connection.execSql(request);
});

